var mdBtnHome = document.getElementById("mdBtnHome");
var mdBtnHomeItems = document.getElementById("mdBtnHomeItems");
var mdBtnClose = document.getElementById("mdBtnClose");

mdBtnHome.onclick = function () {
    if (mdBtnHomeItems.style.display == "none") {
        mdBtnHomeItems.style.display = "block";
        mdBtnClose.style.transform = "rotateZ(135deg)";
        mdBtnClose.style.transition = "1s";
    } else {
        mdBtnHomeItems.style.display = "none";
        mdBtnClose.style.transform = "rotateZ(0deg)";
    }
}

