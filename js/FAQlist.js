var btnHome = document.getElementById("btnHome");
var btnHomeItems = document.getElementById("btnHomeItems");
var btnClose = document.getElementById("btnClose");

btnHome.onclick = function () {
    if (btnHomeItems.style.display == "none") {
        btnHomeItems.style.display = "block";
        btnClose.style.transform = "rotateZ(135deg)";
        btnClose.style.transition = "1s";
    } else {
        btnHomeItems.style.display = "none";
        btnClose.style.transform = "rotateZ(0deg)";
    }
}

